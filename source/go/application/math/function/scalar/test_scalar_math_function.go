package main

// fmt package provides the function to print anything
import (
  "fmt"

  "go/library/math/function/ScalarMathFunction"
)

func main() {
  // define the float32 variables we want to add
  var data_in float32

  var logistic_output float32
  var oneplus_output float32

  // initializing the variables
  data_in = 48.0

  // calling the function and storing the result
  logistic_output = ScalarMathFunction.ScalarLogisticFunction(data_in)
  oneplus_output = ScalarMathFunction.ScalarOneplusFunction(data_in)

  // printing the results
  fmt.Println("logistic: ", data_in, " and ", " is ", logistic_output)
  fmt.Println("oneplus: ", data_in, " and ", " is ", oneplus_output)
  }
