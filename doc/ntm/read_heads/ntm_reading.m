function R_OUT = ntm_reading(W_IN, M_IN)
  [SIZE_N_IN, SIZE_W_IN] = size(M_IN);

  R_OUT = zeros(SIZE_W_IN, 1);

  % r(t;k) = summation(w(t;j)Â·M(t;j;k))[j in 1 to N]

  for j = 1:SIZE_N_IN
    for k = 1:SIZE_W_IN
      R_OUT(j) = R_OUT(j) + W_IN(j)*M_IN(j, k);
    end
  endend